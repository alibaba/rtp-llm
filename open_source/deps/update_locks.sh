#!/bin/bash
# 约束式补锁：单机 uv 交叉解析，只补新增依赖、不升级已锁定包（实跑验证：cuda12 仅 +fsspec）。
# 三个必带机制（均由实跑锁定，见 oss-pip.md 顶部红线 + 二·五）：
#   --constraint <旧lock纯pin>     冻结已锁定版本，避免全量升级
#   --no-emit-package <bazel桶>    bazel 提供的包(torch/flash-attn…)不进 lock；否则会被 autoawq/accelerate 等传递拉回
#   --index-strategy unsafe-best-match  复现 pip 合并语义（安全取舍：靠 OSS 防覆盖治理补偿）
# 升级某包：bash update_locks.sh cuda12 --upgrade-package <name>（透传给 uv，绕过 constraint 冻结）
# 用法: bash deps/update_locks.sh [arch...] [--upgrade-package X ...]   缺省全部 arch
set -e
cd "$(dirname "$0")"
command -v uv >/dev/null || { echo "FAIL: uv 未安装 (pip install uv)"; exit 1; }

IDX=(--index-url https://mirrors.aliyun.com/pypi/simple/
     --extra-index-url https://rtp-opensource.oss-cn-hangzhou.aliyuncs.com/rtp_llm/simple/
     --index-strategy unsafe-best-match)

# arch -> "src lock platform"（与 check_lock_freshness.py / check_lock_consistency.py 保持一致）
declare -A CFG=(
  [cuda12]="requirements_torch_gpu_cuda12.txt requirements_lock_torch_gpu_cuda12.txt x86_64-unknown-linux-gnu"
  [cuda12_9]="requirements_torch_gpu_cuda12_9.txt requirements_lock_torch_gpu_cuda12_9.txt x86_64-unknown-linux-gnu"
  [rocm]="requirements_rocm.txt requirements_lock_rocm.txt x86_64-unknown-linux-gnu"
  [cpu]="requirements_torch_cpu.txt requirements_lock_torch_cpu.txt x86_64-unknown-linux-gnu"
  [arm]="requirements_cpu_arm.txt requirements_lock_torch_arm.txt aarch64-unknown-linux-gnu"
  [cuda12_arm]="requirements_cuda12_arm.txt requirements_lock_cuda12_arm.txt aarch64-unknown-linux-gnu"
)

# 分离 arch 参数与透传给 uv 的参数（如 --upgrade-package X）
ARCHES=(); PASS=()
while [ $# -gt 0 ]; do
  case "$1" in
    --*) PASS+=("$1"); [[ "$1" == "--upgrade-package" ]] && { shift; PASS+=("$1"); } ;;
    *) ARCHES+=("$1") ;;
  esac
  shift
done
[ ${#ARCHES[@]} -eq 0 ] && ARCHES=(cuda12 cuda12_9 rocm cpu arm cuda12_arm)

for a in "${ARCHES[@]}"; do
  read -r src lock plat <<< "${CFG[$a]}"
  echo "== $a: $src -> $lock ($plat)"

  # bazel 桶 -> --no-emit-package（文件缺失=空桶，仅告警）
  noemit=(); bp="bazel_provided_${a}.txt"
  if [ -f "$bp" ]; then
    while read -r name; do
      name="${name%%#*}"; name="$(echo "$name" | tr -d '[:space:]')"
      [ -n "$name" ] && noemit+=(--no-emit-package "${name%%==*}")
    done < "$bp"
  else
    echo "  WARN: 无 $bp（bazel 桶未定义，torch 等可能被拉进 lock）"
  fi

  # 旧 lock 抽纯 pin 做 constraint（冻结传递依赖，避免全量升级）；但自动放开
  # “源里直接声明、且旧 pin 已不满足源 specifier”的包——否则 base 改了某直接包版本时
  # 会与旧 lock 的旧 pin 硬冲突（实测 --upgrade-package 无法绕过硬 == constraint）。
  # uv constraint 不接受 --hash 行，这里只抽纯 pin。
  cons=""
  if [ -f "$lock" ]; then
    cons="$(mktemp)"
    "${PYTHON:-python3}" - requirements_base.txt "$src" "$lock" > "$cons" <<'PY'
import re, sys
from packaging.requirements import Requirement
from packaging.version import Version, InvalidVersion
from packaging.utils import canonicalize_name
*srcs, lock = sys.argv[1:]
specs = {}
for s in srcs:
    try:
        lines = open(s).read().splitlines()
    except OSError:
        continue
    for ln in lines:
        ln = ln.split('#', 1)[0].strip()
        if not ln:
            continue
        try:
            r = Requirement(ln)
        except Exception:
            continue
        specs.setdefault(canonicalize_name(r.name), []).append(r.specifier)
for ln in open(lock):
    m = re.match(r'^([A-Za-z0-9._-]+)==([^ ;\\]+)', ln)
    if not m:
        continue
    raw, ver = m.group(1), m.group(2)
    keep = True
    sset = specs.get(canonicalize_name(raw))
    if sset:
        try:
            v = Version(ver)
            keep = all(sp.contains(v, prereleases=True) for sp in sset)
        except InvalidVersion:
            keep = False
    if keep:
        print(f"{raw}=={ver}")
PY
  fi

  uv pip compile "$src" -o "$lock.tmp" --generate-hashes \
     --python-version 3.10 --python-platform "$plat" --no-header \
     ${cons:+--constraint "$cons"} "${noemit[@]}" "${PASS[@]}" "${IDX[@]}"
  # uv 把临时 --constraint 路径写进 # via 标注；剔除以免机器相关路径进 lock（不影响解析/hash）
  [ -n "$cons" ] && sed -i -E "\|^[[:space:]]*#[[:space:]]+-c[[:space:]]+${cons}[[:space:]]*\$|d" "$lock.tmp"
  [ -n "$cons" ] && rm -f "$cons"

  stamp=$(cat requirements_base.txt "$src" ${bp:+$([ -f "$bp" ] && echo "$bp")} | sha256sum | cut -d' ' -f1)
  {
    echo "# This file is autogenerated by deps/update_locks.sh (uv, 约束式补锁). DO NOT EDIT."
    echo "# input-hash: $stamp"
    cat "$lock.tmp"
  } > "$lock"
  rm -f "$lock.tmp"
done

python3 check_lock_consistency.py . 2>/dev/null || "${PYTHON:-python3}" check_lock_consistency.py .
echo "DONE: ${ARCHES[*]}"
