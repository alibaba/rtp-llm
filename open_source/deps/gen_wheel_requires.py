#!/usr/bin/env python3
"""生成 wheel requires 派生物（单一事实源 + 版本从 lock/bazel 桶派生）。

1) deps/wheel_requires.txt（通用运行时依赖）：
   - 包名 = requirements_base.txt(去 wheel_exclude) + wheel_extra.txt
   - 版本 = 参考 lock(cuda12) 里锁定的版本（**权威**）——wheel METADATA ≡ 实测版本，
     根治「wheel 写 pyarrow==14 但 lock 装 17」这类 skew。base/extra 手写 == 仅作 fallback。
2) deps/wheel_requires_torch_{default,cuda12,rocm}.txt（arch 叶子，🤖生成）：
   - 内容 = 对应 bazel_provided_<arch>.txt（default←cpu, cuda12←cuda12, rocm←rocm）
   - bazel 桶是 wheel arch pin 的唯一真源；arch 专属 pip 增量走 wheel_requires_platform_*。

用法:
  python3 deps/gen_wheel_requires.py            # 生成/覆盖 全部派生物
  python3 deps/gen_wheel_requires.py --check     # 只校验是否最新（CI gate，不写文件）
"""
import re
import sys
from pathlib import Path

DEPS = Path(__file__).parent
BASE = DEPS / "requirements_base.txt"
EXCLUDE = DEPS / "wheel_exclude.txt"
EXTRA = DEPS / "wheel_extra.txt"
OUT = DEPS / "wheel_requires.txt"
REF_LOCK = DEPS / "requirements_lock_torch_gpu_cuda12.txt"  # x86 参考；G3 保证同平台一致
# wheel arch 叶子 ← bazel 桶（BUILD 的 select 只有 default/cuda12/rocm 三轴）
LEAF_MAP = {"default": "cpu", "cuda12": "cuda12", "rocm": "rocm"}

NAME_RE = re.compile(r"^([A-Za-z0-9][A-Za-z0-9._-]*)")


def norm(name):
    return name.lower().replace("_", "-").replace(".", "-")


def read_names(path):
    names = set()
    for line in path.read_text().splitlines():
        s = line.strip()
        if not s or s.startswith("#"):
            continue
        m = NAME_RE.match(s)
        if m:
            names.add(norm(m.group(1)))
    return names


def lock_versions(path):
    """{norm_name: version} from a lock file."""
    out = {}
    if not path.exists():
        return out
    for line in path.read_text().splitlines():
        m = re.match(r"^([A-Za-z0-9][A-Za-z0-9._-]*)==([^ ;\\]+)", line.strip())
        if m:
            out[norm(m.group(1))] = m.group(2)
    return out


def generate():
    exclude = read_names(EXCLUDE)
    ver = lock_versions(REF_LOCK)
    lines = ["# GENERATED by deps/gen_wheel_requires.py（名来自 requirements_base+wheel_extra，版本来自 cuda12 lock）。DO NOT EDIT.",
             "# 改通用依赖 → requirements_base.txt 后重编 lock；改排除/增量 → wheel_exclude.txt / wheel_extra.txt。"]

    def emit(raw_line):
        s = raw_line.split(";")[0].strip()
        m = NAME_RE.match(s)
        if not m:
            return None
        n = norm(m.group(1))
        if n in exclude:
            return None
        display = m.group(1)                 # 保留原始大小写/写法
        if n in ver:                         # 版本以 lock 为准
            return f"{display}=={ver[n]}"
        return s                              # lock 里没有（如纯 extra 未锁包）→ 保留原样

    seen = set()
    for src in (BASE, EXTRA):
        for line in src.read_text().splitlines():
            s = line.strip()
            if not s or s.startswith("#") or s.startswith("-"):
                continue
            e = emit(s)
            if e and e not in seen:
                seen.add(e)
                lines.append(e)
    return "\n".join(lines) + "\n"


def gen_leaf(leaf, arch):
    """wheel_requires_torch_<leaf>.txt 内容 ← bazel_provided_<arch>.txt（注释剥离，顺序保留）。"""
    src = DEPS / f"bazel_provided_{arch}.txt"
    lines = [f"# GENERATED by deps/gen_wheel_requires.py（内容来自 bazel_provided_{arch}.txt，bazel 桶=唯一真源）。DO NOT EDIT.",
             f"# 改 arch pin → deps/bazel_provided_{arch}.txt；arch 专属 pip 增量 → wheel_requires_platform_*。"]
    for line in src.read_text().splitlines():
        s = line.split("#", 1)[0].strip()
        if s:
            lines.append(s)
    return "\n".join(lines) + "\n"


def main():
    targets = [(OUT, generate())]
    for leaf, arch in LEAF_MAP.items():
        targets.append((DEPS / f"wheel_requires_torch_{leaf}.txt", gen_leaf(leaf, arch)))

    if "--check" in sys.argv:
        stale = [p.name for p, content in targets
                 if (p.read_text() if p.exists() else "") != content]
        if stale:
            print(f"FAIL: {' '.join(stale)} 过期或与真源不一致，跑 python3 deps/gen_wheel_requires.py")
            sys.exit(1)
        print(f"OK: wheel_requires.txt + {len(LEAF_MAP)} 个 torch 叶子 均为最新派生（版本与 lock/bazel 桶一致）")
    else:
        for p, content in targets:
            p.write_text(content)
        n = sum(1 for l in targets[0][1].splitlines() if l and not l.startswith("#"))
        print(f"WROTE {OUT.name}: {n} 个包（版本取自 {REF_LOCK.name}）; "
              f"torch 叶子 ×{len(LEAF_MAP)}（内容取自 bazel_provided_*）")


if __name__ == "__main__":
    main()
